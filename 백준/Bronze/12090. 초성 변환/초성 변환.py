def trans(korean_word):
    r_lst = []
    for w in list(korean_word.strip()):
        ch1 = (ord(w) - ord('가')) // 588
        ch2 = ((ord(w) - ord('가')) - (588 * ch1)) // 28
        ch3 = (ord(w) - ord('가')) - (588 * ch1) - 28 * ch2
        r_lst.append([first[ch1], second[ch2], last[ch3]])
    return r_lst

letter = input()
letter_list = trans(letter)
for now_word in letter_list:
    print(now_word[0], end = '')